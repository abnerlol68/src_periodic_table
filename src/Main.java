    private void elementSelection(int numberAtomic) {
        MakeQuery query = new MakeQuery(numberAtomic);

        query.consult();

        this.txtSymbol.setText(query.getSymbol());
        this.txtName.setText(query.getName());
        this.txtAtomicNumber.setText(query.getNumberAtomic());
        this.txtAtomicMass.setText(query.getMass());
        this.txtElectronicConfiguration.setText(query.getElecConfiguration());
        this.txtElectronegativity.setText(query.getElectronegativity());
        this.txtYear.setText(query.getYear());

        this.elementImg = new ImageIcon(getClass().getResource("./img/" + query.getSrcImg()));
        this.lblImg.setIcon(elementImg);
    }

